#operadores aritmeticos

x=35
y=6

#suma
print("Resultado de la suma= ", x+y)

#resta
print("Resultado de la resta= ", x-y)

#multiplicacion
print("Resultado de la multiplicacion= ", x*y)

#division
print("Resultado de la division= ", x/y)

#producto entero de una division //
print("Resultado de la division entera= ", x//y)

#esponente
print("Resultado del exponente= ", x**y)



#operadores comparativos 
#mayor que
print("X es mayor que Y= ", x>y)

#menor que
print("X es menor que Y= ", x<y)

#igual a
print("X es igual a Y= ", x==y)

#no igual a
print("X es diferente a Y= ", x!=y)

#mayor o igual
print("X es mayor o igual a Y= ", x>=y)

#menor o igual
print("X es menor o igual a Y= ", x<=y)



